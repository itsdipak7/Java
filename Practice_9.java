public class Practice_9 {
    public static void main(String[] args) {

        //Java provides short cut operators to combine an Arithematic operator and assignment operator.

//        for example ,
//        i=i+5;

//       can also be rewritten as
//       i+=4;

        int i=5;
        int j=10;
        i+=5;  //Same as i-i+5;
        j-=2; //Same as j=j-2
        System.out.println("i="+i);
        System.out.println("j="+j);
    }
}
